def area(a, b):
    '''
    Принимает значания двух сторон прямоугольника, возвращает его площадь
    Пример: input:5,4 output:20
    '''
    return a * b 

def perimeter(a, b):
    '''
    Принимает значения двух сторон прмоугольника, возвращает его периметр
    Пример: input:5,4 output:18
    '''
    return 2*(a + b)