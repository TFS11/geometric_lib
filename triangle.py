def area(a, h):
    '''
    Принимает значение стороны и проведенной к ней высоте треугольника, возвращает его площадь
    Пример: input:5,4 output:10
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Принимает значения трех сторон треугольника, возвращает его периметр
    Пример: input:5,10,6 output:21
    '''
    return a + b + c